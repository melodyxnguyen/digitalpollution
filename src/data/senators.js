export const stateNames = Object.keys(senatorsByState).sort()

export function generateEmailDraft(senators, state, concernId, personalNote) {
  const names = senators.map(s => s.name.split(' ').slice(-1)[0])
  const salutation = names.length > 1
    ? `Dear Senators ${names[0]} and ${names[1]},`
    : `Dear Senator ${names[0]},`

  const concerns = {
    energy: {
      about: 'the growing energy consumption of AI infrastructure',
      body: `Data centers supporting large AI systems now account for an estimated 1–2% of global electricity demand, with projections suggesting this could triple by 2030. Unlike other major industries, AI companies currently face no mandatory requirement to publicly disclose their energy use — leaving regulators and the public unable to assess the true scale of this growth.`,
    },
    water: {
      about: 'the significant water consumption required to cool AI data centers',
      body: `Training and running AI systems requires enormous volumes of freshwater for cooling. As data centers expand into drought-prone regions, this hidden cost places measurable strain on local water supplies and ecosystems. Yet most AI companies publish little or no data on their water usage, and no federal reporting requirement exists.`,
    },
    transparency: {
      about: 'the lack of corporate transparency around AI\'s environmental footprint',
      body: `Most major AI companies publish little or no data on their energy and water consumption. Without mandatory reporting requirements, the public has no basis for evaluating whether the benefits of AI justify its growing impact on shared infrastructure. Transparency is a prerequisite for accountability — and right now, it is absent.`,
    },
    regulation: {
      about: 'the urgent need for stronger federal oversight of AI infrastructure',
      body: `The rapid expansion of AI data centers is outpacing the regulatory frameworks designed to protect communities and public resources. Local ratepayers and municipalities are increasingly asked to absorb costs — in electricity demand, water use, and infrastructure strain — that should properly be borne by the technology companies that profit from them.`,
    },
    all: {
      about: 'the full environmental and infrastructure costs of artificial intelligence',
      body: `From the electricity consumed during model training and inference, to the water used to cool data centers, AI imposes significant and growing costs on shared public resources. These costs are largely invisible because there are no federal requirements for AI companies to disclose or account for them — and because the infrastructure buildout is moving faster than our regulatory capacity to respond.`,
    },
  }

  const { about, body } = concerns[concernId] || concerns.all

  const personalPara = personalNote?.trim()
    ? `\nI also want to share a personal perspective: ${personalNote.trim()}\n`
    : ''

  return `${salutation}

I am a constituent in ${state} writing to urge your action on ${about}.

${body}
${personalPara}
I am asking you to champion the following measures in the Senate:

1. Mandatory energy and water usage reporting for AI companies operating at scale, with public disclosure requirements comparable to those in other regulated industries

2. Stronger federal oversight of data center siting, planning, and operations — including honest accounting of the infrastructure costs borne by local communities and ratepayers

3. A legal and corporate governance framework that ensures the environmental and social costs of AI infrastructure are borne by the companies that benefit, not socialized to the public

The decisions made in the next few years about how to govern AI infrastructure will shape its environmental impact for decades. As a senator representing ${state}, you have the opportunity — and I would argue the responsibility — to ensure that this technology grows in a way that is accountable to the public it serves.

Thank you for your time and your service.

Respectfully,
[Your Name]
[Your City, ${state}]`
}
